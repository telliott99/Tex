def str_xor(c1,c2):
    if c1 == c2:
        return '0'
    return '1'

# binary strings, no '0b', one cycle
def div(a,b):
    diff = len(a) - len(b)
    if diff < 0:
        return a
    b = b + '0'*diff
    L = [str_xor(x,y) for x,y in zip(a,b)]
    # remove leading '0'
    while L and L[0] == '0':
        L.pop(0)
    if not L:
        return '0'
    return ''.join(L)

def string_div(a,b):
    rL = list()
    while len(a) >= len(b):
        a = div(a,b)
        rL.append(a)
    return rL

def fmt(a,b,L):
    n = len(a)
    pL = list()
    for r in L:
        pad = ' ' * (n - len(a))
        pL.append(pad + a)
        pL.append(pad + b)
        pL.append(pad + '-' * len(b))
        a = r
    last = a.rjust(len(pad + b))
    pL.append(last)
    return '\n'.join(pL)

